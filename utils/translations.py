def get_translation(user_id: int, key: str, custom_db=None, **kwargs) -> str:
    from database.db import db as default_db
    active_db = custom_db or default_db
    user = active_db.get_user(user_id)
    
    lang = 'en'
    if user:
        if isinstance(user, dict):
            lang = user.get('lang', 'en')
        else:
            try:
                lang = user['lang']
            except (IndexError, KeyError, TypeError):
                try:
                    lang = user[4]
                except Exception:
                    lang = 'en'

    lang_translations = translations.get(lang, translations['en'])
    translation_text = lang_translations.get(key, '')
    return translation_text.format(**kwargs)
